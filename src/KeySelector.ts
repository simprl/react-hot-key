export const DELETE = (e: KeyboardEvent): boolean => e.key === 'Delete';
export const ENTER = (e: KeyboardEvent): boolean => e.key === 'Enter';
export const ESC = (e: KeyboardEvent): boolean => e.key === 'Escape';
export const ARROW_UP = (e: KeyboardEvent): boolean => e.key === 'ArrowUp';
export const ARROW_DOWN = (e: KeyboardEvent): boolean => e.key === 'ArrowDown';
export const ARROW_LEFT = (e: KeyboardEvent): boolean => e.key === 'ArrowLeft';
export const ARROW_RIGHT = (e: KeyboardEvent): boolean => e.key === 'ArrowRight';
export const SPACE = (e: KeyboardEvent): boolean => e.key === ' ';
